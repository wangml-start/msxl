package com.cgmn.msxl.data;

public class UserDetailInfo {
    String vipType;
    String vipEnd;
    String TotalAmt;
    String TotalPl;
    String trainTimes;
    String winRate;
    String accDan;
    String birthDay;

    public String getVipType() {
        return vipType;
    }

    public void setVipType(String vipType) {
        this.vipType = vipType;
    }

    public String getVipEnd() {
        return vipEnd;
    }

    public void setVipEnd(String vipEnd) {
        this.vipEnd = vipEnd;
    }

    public String getTotalAmt() {
        return TotalAmt;
    }

    public void setTotalAmt(String totalAmt) {
        TotalAmt = totalAmt;
    }

    public String getTotalPl() {
        return TotalPl;
    }

    public void setTotalPl(String totalPl) {
        TotalPl = totalPl;
    }

    public String getTrainTimes() {
        return trainTimes;
    }

    public void setTrainTimes(String trainTimes) {
        this.trainTimes = trainTimes;
    }

    public String getWinRate() {
        return winRate;
    }

    public void setWinRate(String winRate) {
        this.winRate = winRate;
    }

    public String getAccDan() {
        return accDan;
    }

    public void setAccDan(String accDan) {
        this.accDan = accDan;
    }

    public String getBirthDay() {
        return birthDay;
    }

    public void setBirthDay(String birthDay) {
        this.birthDay = birthDay;
    }
}
